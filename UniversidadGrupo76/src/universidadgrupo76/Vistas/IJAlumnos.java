    private void jbbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbbuscarActionPerformed
        // TODO add your handling code here:
        try {
            AlumnoData alu = new AlumnoData();
            int dni = Integer.parseInt(jtdocumento.getText());

            Alumno alumnoEncontrado = alu.buscarAlumnoPordni(dni);

            if (alumnoEncontrado != null) {
                idalumno = alumnoEncontrado.getIdAlumno();
                jtapellido.setText(alumnoEncontrado.getApellido());
                jtnombre.setText(alumnoEncontrado.getNombre());
                jrActivo.setSelected(true);
                jDateChooser1.setDate(java.sql.Date.valueOf(alumnoEncontrado.getFechaNacimiento()));
                JOptionPane.showMessageDialog(null, "Alumno encontrado.");
            } else {
                JOptionPane.showMessageDialog(null, "Alumno no encontrado.");
            }
        } catch (NumberFormatException nf) {
            JOptionPane.showMessageDialog(null, "Debe ingresar solo números enteros");
        }

    }//GEN-LAST:event_jbbuscarActionPerformed

    private void jbeliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbeliminarActionPerformed

        if (idalumno > 0) {
            AlumnoData alu = new AlumnoData();
            alu.eliminarAlumno(idalumno);
            JOptionPane.showMessageDialog(null, "Alumno eliminado exitosamente.");
            jtdocumento.setText("");
            jtapellido.setText("");
            jtnombre.setText("");
             jDateChooser1.setDate(null);
            jrActivo.setSelected(false);
        } else {
            JOptionPane.showMessageDialog(null, "Selecciona un alumno válido para eliminar.");
        }
    }//GEN-LAST:event_jbeliminarActionPerformed

