    private void jbnuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbnuevoActionPerformed

        boolean estado = false;
        int documento = 0;
        String apellido = "";
        String nombre = "";

        try {
            documento = Integer.parseInt(jtdocumento.getText());
            if (jtapellido.getText().isEmpty() || jtnombre.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "No pueden quedar campos vacios");
                return;
            }
            apellido = jtapellido.getText();
            nombre = jtnombre.getText();

        } catch (NumberFormatException nf) {
            JOptionPane.showMessageDialog(null, "Deve ingresar solo nsumero enteros");

        }

        if (jDateChooser1.getDate() != null) {
            LocalDate fechaN = jDateChooser1.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();

            // DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd");
            if (jrActivo.isSelected()) {
                estado = true;
            }

            Alumno nuevo = new Alumno(documento, nombre, apellido, fechaN, estado);

            AlumnoData alu = new AlumnoData();

            try {
                alu.guardarAlumno(nuevo);
                jtdocumento.setText("");
                jtapellido.setText("");
                jtnombre.setText("");
            } catch (SQLException ex) {
                Logger.getLogger(IJAlumnos.class.getName()).log(Level.SEVERE, null, ex);

            }
        }


    }//GEN-LAST:event_jbnuevoActionPerformed

