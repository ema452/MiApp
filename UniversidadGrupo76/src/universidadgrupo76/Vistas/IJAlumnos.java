private void agregarListenerJtcodigo() {
    jtdocumento.getDocument().addDocumentListener(new DocumentListener() {
        @Override
        public void insertUpdate(DocumentEvent e) {
            bloquearBoton(); // Llama al método que verifica y ajusta el estado del botón
        }

        @Override
        public void removeUpdate(DocumentEvent e) {
            bloquearBoton(); // Llama al método que verifica y ajusta el estado del botón
        }

        @Override
        public void changedUpdate(DocumentEvent e) {
            // Este método se utiliza para campos que no son de texto, como JComboBox, no es relevante aquí.
        }
    });
}


private void bloquearBoton(){
    if (!jtdocumento.getText().isEmpty()) {
        jbeliminar.setEnabled(true);
        jbbuscar.setEnabled(true);
        jbnuevo.setEnabled(true);
        jbguardar.setEnabled(true);
    }else{
    jbeliminar.setEnabled(false);
        jbbuscar.setEnabled(false);
        jbnuevo.setEnabled(false);
        jbguardar.setEnabled(false);
    }
        
    
}

