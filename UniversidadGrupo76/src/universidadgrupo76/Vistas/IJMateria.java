private void agregarListenerJtcodigo() {
    // Agregamos un DocumentListener al campo jtnombre
    jtnombre.getDocument().addDocumentListener(new DocumentListener() {
        @Override
        public void insertUpdate(DocumentEvent e) {
            bloquearBoton();
        }

        @Override
        public void removeUpdate(DocumentEvent e) {
            bloquearBoton();
        }

        @Override
        public void changedUpdate(DocumentEvent e) {
            // No es relevante aquí
        }
    });
    
    // Agregamos un DocumentListener al campo jtcodigo
    jtcodigo.getDocument().addDocumentListener(new DocumentListener() {
        @Override
        public void insertUpdate(DocumentEvent e) {
            bloquearBoton();
        }

        @Override
        public void removeUpdate(DocumentEvent e) {
            bloquearBoton();
        }

        @Override
        public void changedUpdate(DocumentEvent e) {
            // No es relevante aquí
        }
    });
}

private void bloquearBoton() {
    // Verificamos si alguno de los campos (jtcodigo o jtnombre) está vacío
    if (jtcodigo.getText().isEmpty() && jtnombre.getText().isEmpty()) {
        habilitarBotones(false); // Si ambos están vacíos, deshabilitamos los botones
    } else {
        habilitarBotones(true); // Si alguno de los dos tiene contenido, habilitamos los botones
    }
}

private void habilitarBotones(boolean habilitar) {
    jbeliminar.setEnabled(habilitar);
    jbbuscar.setEnabled(habilitar);
    jbnuevo.setEnabled(habilitar);
    jbguardar.setEnabled(habilitar);
}
