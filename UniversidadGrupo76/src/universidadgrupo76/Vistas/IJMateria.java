    private void jbnuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbnuevoActionPerformed
        // TODO add your handling code here:
        try{
        if (!jtcodigo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "El campo codigo tiene que estar vacio");
            jtcodigo.setText("");
        }
        String nombre = jtnombre.getText();
        int año = Integer.parseInt(jtaño.getText());
        boolean estado = jractivo.isSelected();
        Materia nuevaMateria = new Materia(nombre, año, estado);
        mata.guardarMateria(nuevaMateria);
        jtnombre.setText("");
        jtaño.setText("");
        jractivo.setSelected(false);
        }catch (NumberFormatException  e){
                    JOptionPane.showMessageDialog(null, "Por favor, ingresa un año válido.");

        }
    }//GEN-LAST:event_jbnuevoActionPerformed

    private void jbbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbbuscarActionPerformed
        // TODO add your handling code here:
        if (jtcodigo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "El campo codigo no tiene que estar vacio");
            return;
        }
        try{
        int codigo=Integer.parseInt(jtcodigo.getText());
       Materia materiaEncontrada=mata.buscarMateria(codigo);
       if(materiaEncontrada!=null){
           JOptionPane.showMessageDialog(null, "Materia encontrada");
        jtnombre.setText(materiaEncontrada.getNombre());
        jtaño.setText(materiaEncontrada.getAno()+"");
        jractivo.setSelected(materiaEncontrada.isEstado());
    }
       } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "El campo código debe ser un número válido");
    }//GEN-LAST:event_jbbuscarActionPerformed
    }
    private void jbeliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbeliminarActionPerformed
        // TODO add your handling code here:
        try{
        int codigo=Integer.parseInt(jtcodigo.getText());
        if (!jtcodigo.getText().isEmpty()) {
            mata.EliminadoLogiMateria(codigo);
            JOptionPane.showMessageDialog(null, "Materia Eliminada con exito");
            jtcodigo.setText("");
           jtnombre.setText("");
        jtaño.setText("");
        jractivo.setSelected(false); 
        }else{
                    JOptionPane.showMessageDialog(null, "No se puede eliminar la materia");

        }
         } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "El campo código debe ser un número válido");
    }        
    }//GEN-LAST:event_jbeliminarActionPerformed

